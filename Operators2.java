import java.util.*;
public class Operators2 {
   public static void main(String args[])
   {
    int x=10;
    System.out.println();
    System.out.println(x+=5);
    System.out.println(x-=5);
    System.out.println(x/=5);
    System.out.println(x*=5);
    System.out.println(x&=5);
    System.out.println(x|=5);
    System.out.println(x<<=5);
    System.out.println(x>>=5);

   } 
}
